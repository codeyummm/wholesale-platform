export function PlatformLogo({ platform, size = 24, grey = false, className = '' }) {
  const logo = LOGOS[platform?.toLowerCase()];

  const style = {
    width: size,
    height: size,
    display: 'inline-flex',
    alignItems: 'center',
    justifyContent: 'center',
    flexShrink: 0,
  };

  const svgStyle = {
    width: size,
    height: size,
    color: grey ? '#9ca3af' : (logo?.color || '#6b7280'),
    filter: grey ? 'none' : undefined,
    transition: 'color 0.15s',
  };

  if (!logo) {
    // Fallback: first letter avatar
    return (
      <span
        className={`inline-flex items-center justify-center rounded-full bg-gray-200 text-gray-500 font-bold ${className}`}
        style={{ width: size, height: size, fontSize: size * 0.45 }}
      >
        {platform?.[0]?.toUpperCase() || '?'}
      </span>
    );
  }

  return (
    <span style={style} className={className}>
      {React.cloneElement(logo.svg, { style: svgStyle })}
    </span>
  );
}

